    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String operacionTexto = jComboBoxOperacion.getSelectedItem().toString();
            char operacion = '_';
            if ("suma".equals (operacionTexto)){
                operacion = '+';
            }
            if ("resta".equals(operacionTexto)){
                operacion = '-';
            }
            if ("multiplicacion".equals(operacionTexto)){
                operacion = '*';
            }            
            
            Long n0 = new Long(jTextFieldN0.getText());
            Long n1 = new Long(jTextFieldN1.getText());
            Long modulo = new Long(jTextFieldModulus.getText());
            Integer iteraciones = new Integer(jTextFieldIteraciones.getText());
                      
            p.inicio(n0, n1, modulo, operacion, iteraciones);

            p.operar();
            
            jTextAreaResultado.setText(p.imprimirResultados());

        }catch (Exception ex) {
            jTextAreaResultado.setText(ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

